define([
    "blocks/parser/matchChar",
    "../../blocks/string/utf16Encode",
    "polyfill/has!String.codePointAt"
], function (matchChar, utf16Encode) {
    /**
     * [164] PN_CHARS_BASE ::= [A-Z] | [a-z] | [#x00C0-#x00D6] | [#x00D8-#x00F6] | [#x00F8-#x02FF] | [#x0370-#x037D] | [#x037F-#x1FFF] | [#x200C-#x200D] | [#x2070-#x218F] | [#x2C00-#x2FEF] | [#x3001-#xD7FF] | [#xF900-#xFDCF] | [#xFDF0-#xFFFD] | [#x10000-#xEFFFF]
     * @see http://www.w3.org/TR/sparql11-query/#rPN_CHARS_BASE
     * @property {blocks.parser.Data} data
     * @return {RdfJs.Node | Null}
     */
    function pnCharsBase(data) {
        //[163s] PN_CHARS_BASE ::= [A-Z] | [a-z] | [#x00C0-#x00D6] | [#x00D8-#x00F6] | [#x00F8-#x02FF] | [#x0370-#x037D] | [#x037F-#x1FFF] | [#x200C-#x200D] | [#x2070-#x218F] | [#x2C00-#x2FEF] | [#x3001-#xD7FF] | [#xF900-#xFDCF] | [#xFDF0-#xFFFD] | [#x10000-#xEFFFF]
        var ch = matchChar(data, "[A-Z]|[a-z]|[\xC0-\xD6]|[\xD8-\xF6]|[\u00F8-\u02FF]|[\u0370-\u037D]|[\u037F-\u1FFF]|[\u200C-\u200D]|[\u2070-\u218F]|[\u2C00-\u2FEF]|[\u3001-\uD7FF]|[\uF900-\uFDCF]|[\uFDF0-\uFFFD]");
        if (ch === null) {
            var code = data.input.codePointAt(data.pos);
            if (code >= 0x10000 && code <= 0xEFFFF) {
                data.pos += 2;
                return utf16Encode("0x" + code.toString(16));
            }
        }
        return ch;
    }

    return pnCharsBase;
});